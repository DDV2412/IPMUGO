          this.success = true

          this.callback =
            'Thank you for contacting us. We will get back to you as soon as possible.'
        }
      } catch (error: any) {
        this.success = false
        this.error = true

        this.callback =
          'An error occurred while sending your message. Please try again later or contact customer support for assistance.'
      } finally {
        this.loading = false
      }
    },
  },
})
</script>
